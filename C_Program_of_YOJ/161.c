/*某个公司采用公用电话传递数据，数据是四位的整数，数据在传递过程中是加密的：每位数字都加上5，
得到的结果除以10的余数代替该数字，再将第一位和第四位交换，第二位和第三位交换*/
#include<stdio.h>
int main(){
    int n;
    scanf("%d",&n);
    int q,b,s,g;
    g=((n%10)+5)%10;n=n-n%10;
    s=((n%100)/10+5)%10;n=n-n%100;
    b=((n%1000)/100+5)%10;n=n-n%1000;
    q=((n%10000)/1000+5)%10;
    printf("%d",g*1000+s*100+b*10+q);
    return 0;
}